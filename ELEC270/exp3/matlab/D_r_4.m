%% Part D reqiurement 4

close all

f_0 = 1e3;

syms a(t) b(t) c(t)

a(t) = ...
    rdivide(3,pi) * ( ...
        - cos(2*pi*f_0*t) ...
        + 1/3 * cos(2*pi*3*f_0*t) ...
        - 1/5 * cos(2*pi*5*f_0*t) ...
        + 1/7 * cos(2*pi*7*f_0*t) ...
        - 1/9 * cos(2*pi*9*f_0*t) ...
    );
subplot(2,2,1), fplot(a, [0, .002]);
title("(a)");

b(t) = ...
    rdivide(3,pi) * ( ...
        - cos(2*pi*f_0*t) ...
        - 1/3 * cos(2*pi*3*f_0*t) ...
        - 1/5 * cos(2*pi*5*f_0*t) ...
        - 1/7 * cos(2*pi*7*f_0*t) ...
        - 1/9 * cos(2*pi*9*f_0*t) ...
    );
subplot(2,2,2), fplot(b, [0, .002]);
title("(b)");

c(t) = ...
    rdivide(3,pi) * ( ...
        1/2 * 1/3 * cos(2*pi*3*f_0*t) ...
        - 1/5 * cos(2*pi*5*f_0*t) ...
        + 1/7 * cos(2*pi*7*f_0*t) ...
        - 1/2 * 1/9 * cos(2*pi*9*f_0*t) ...
    );
subplot(2,2,3), fplot(c, [0, .002]);
title("(c)");
